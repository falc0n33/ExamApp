package com.falc0n.android.examapp.algs.diffi_hellman;

public class Pair {
	private Object u;
	private Object v;

	public Pair() {
		u = null;
		v = null;
	}

	public Object getU() {
		return u;
	}

	public void setU(Object u) {
		this.u = u;
	}

	public Object getV() {
		return v;
	}

	public void setV(Object v) {
		this.v = v;
	}
	
	
}
